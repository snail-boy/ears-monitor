<template>
  <a-card>
    <div :class="advanced ? 'search' : null">
      <a-form layout="horizontal" :form="form" @submit="searchList">
        <div :class="advanced ? null: 'fold'">
          <a-row>
            <a-col :md="10" :sm="24">
              <a-form-item
                      label="缴费人姓名"
                      :labelCol="{span: 5}"
                      :wrapperCol="{span: 18, offset: 1}"
              >
                <a-input
                        v-decorator="['applicantName', { rules: [{ required: false}] }]"
                        placeholder="请输入"
                />
              </a-form-item>
            </a-col>
            <a-col :md="14" :sm="24">
              <a-form-item
                      label="缴费人证件号码"
                      :labelCol="{span: 5}"
                      :wrapperCol="{span: 18, offset: 1}"
              >
                <a-input
                        v-decorator="['applicantCertNo', { rules: [{ required: false}] }]"
                        style="width: 100%" placeholder="请输入"/>
              </a-form-item>
            </a-col>
          </a-row>
          <a-row v-if="advanced">
            <a-col :md="10" :sm="24">
              <a-form-item
                      label="录屏开始时间"
                      :labelCol="{span: 5}"
                      :wrapperCol="{span: 18, offset: 1}"
              >
                <a-date-picker
                        format="YYYY-MM-DD HH:mm:ss"
                        v-decorator="['beginTime', { rules: [{ required: false}] }]"
                        :show-time="{ defaultValue: moment('00:00:00', 'HH:mm:ss') }"
                        style="width: 100%" placeholder="选择时间"/>
              </a-form-item>
            </a-col>
          </a-row>
        </div>
        <span style="float: right; margin-top: 3px;">
          <a-button type="primary" @click="searchList">查询</a-button>
          <a-button style="margin-left: 8px">重置</a-button>
          <a @click="toggleAdvanced" style="margin-left: 8px">
            {{advanced ? '收起' : '展开'}}
            <a-icon :type="advanced ? 'up' : 'down'"/>
          </a>
        </span>
      </a-form>
    </div>
    <div>
      <standard-table
              :columns="columns"
              :dataSource="dataSource"
              rowKey="orderNo"
              :loading="loading"
              :pagination="{
              current: pageNo,
              pageSize: pageSize,
              total: total,
              showSizeChanger: true,
              showLessItems: true,
              showQuickJumper: true,
              showTotal: (total, range) => `第 ${range[0]}-${range[1]} 条，总计 ${total} 条`,
              onChange: onPageChange,
              onShowSizeChange: onSizeChange,
            }"
      >
         <span slot="duration" slot-scope="{text}">
          {{ formatTime(text) }}
        </span>
        <span slot="fileSize" slot-scope="{text}">
          {{ byteToKb(text) }}
        </span>
        <div slot="action" slot-scope="{text, record}">
          <a v-if="record.status === '生效'" style="margin-right: 8px" @click="handleRecord(record)">
            播放视频
          </a>
        </div>
      </standard-table>
    </div>
  </a-card>
</template>

<script>
  import StandardTable from '@/components/table/StandardTable'
  import {monitorByOrderNoUrl, monitorMainOneUrl} from "../../../services/dataSource";
  import {S_OK} from "../../../utils/constant";
  import moment from "moment";
  import {mapMutations} from "vuex";
  import {byteToKb, formatDuring} from "../../../utils/util";

  const columns = [
    {
      title: '序号',
      dataIndex: 'key'
    },
    {
      title: '订单ID',
      dataIndex: 'orderNo'
    },
    {
      title: '产品Code',
      dataIndex: 'productCode'
    },
    {
      title: '缴费人姓名',
      dataIndex: 'applicantName'
    },
    {
      title: '缴费人证件类型',
      dataIndex: 'applicantType'
    },
    {
      title: '缴费人证件号码',
      dataIndex: 'applicantCertNo'
    },
    {
      title: '设备号',
      dataIndex: 'equipment',
      ellipsis: true,
    },
    {
      title: '开始时间',
      dataIndex: 'createTime'
    },
    {
      title: '视频时长',
      dataIndex: 'duration',
      scopedSlots: { customRender: "duration" }
    },
    {
      title: '文件大小',
      dataIndex: 'fileSize',
      scopedSlots: { customRender: "fileSize" }
    },
    {
      title: '视频状态',
      dataIndex: 'status'
    },
    {
      title: '过期时间',
      dataIndex: 'expireTime'
    },
    {
      title: '操作',
      dataIndex: 'action',
      scopedSlots: { customRender: 'action'}
    }
  ]

  export default {
    name: 'QueryList',
    components: {StandardTable},
    data() {
      return {
        advanced: true,
        columns: columns,
        dataSource: [],
        form: this.$form.createForm(this),
        pageNo: 1,
        pageSize: 10,
        total: 0,
        conditions: {
          applicantName: '',
          applicantCertNo: '',
          beginTime: ''
        },
        travel: [],
        loading: true
      }
    },
    authorize: {
      deleteRecord: 'delete'
    },
    created() {
      this.getDataList()
    },
    methods: {
      moment,
      ...mapMutations('content', ['setVideoEvents']),
      formatTime(time) {
        return formatDuring(time)
      },
      byteToKb(byte) {
        return byteToKb(byte)
      },
      searchList() {
        this.form.validateFields((err, values) => {
          if (!err) {
            console.log('Received values of form: ', values);
            this.conditions = values
            this.conditions.beginTime = moment(values.beginTime).format('YYYY-MM-DD HH:mm:ss')
            this.getDataList()
          }
        })
      },
      async getDataList() {
        const {pageNo, pageSize, conditions} = this
        let result = await monitorMainOneUrl({pageNo, pageSize, ...conditions})
        const {list, total} = result.data.result
        this.dataSource = list.map((val, index) => {
          return Object.assign({}, val, {key: index + 1})
        })
        this.pageNo = pageNo
        this.total = total
        this.pageSize = pageSize
        this.loading = false
      },
      async handleRecord(v) {
        this.loading = true
        const result = await monitorByOrderNoUrl({orderNo: v.orderNo})
        if (result.data.code === S_OK) {
          this.travel = result.data.result.tracks
          const events = result.data.result.events
          if(!events.length) {
            this.$message.warning('无数据， 无法播放')
          }else {
            this.setVideoEvents(result.data.result.events)
            this.$router.push('/monitor/replay')
          }
          this.loading = false
        }
      },
      toggleAdvanced() {
        this.advanced = !this.advanced
      },
      onPageChange(pageNo, pageSize) {
        this.pageNo = pageNo
        this.pageSize = pageSize
        this.getDataList()
      },
      onSizeChange(current, size) {
        this.pageNo = 1
        this.pageSize = size
        this.getDataList()
      },
      onReset(conditions) {
        this.conditions = conditions
        this.getDataList()
      },
      onRefresh(conditions) {
        this.conditions = conditions
        this.getDataList()
      }
    }
  }
</script>

